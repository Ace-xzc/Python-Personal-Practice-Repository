
# 数值 : int整形(整数)
#       float浮点类型(小数)
# 布尔类型 : Ture (1)  False (0)
# 字符串类型 : 用引号引起来的数据 '324' '你好' '2006' 字符独立成数据 ''
# 列表类型 : 用中括号括起来的数据, 每个数据用逗号隔开 ['2006','06','28'] []
# 元组类型 : 用小括号括起来的数据, 每个数据用逗号隔开 ('2006',06,['28']) 可以存放任意类型数据 ()
# 集合类型 : 用大括号括起来的数据, 每个数据用逗号隔开 {12,'12'} 可以存放任意类型数据
# 字典类型 : 用大括号括起来的数据, 每个键值对用逗号隔开 {'python':90,'birthday':2006 }  {}是字典
#                                             两个键值对 冒号左边是键 右边是值 左边一般是字符串类型
#空值类型 : 值未确定时用None
