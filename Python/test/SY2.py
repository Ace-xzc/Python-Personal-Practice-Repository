import turtle as t
import math
t.screensize(600, 600)
t.penup()
t.goto(-250, 150)
t.pendown()
t.write('20255120801091 廖滢滢 ',font=('华文琥珀',20,'normal'))
t.penup()
t.goto(-150, 60)
t.pendown()
t.pensize(6)
t.pencolor('blue')
t.fillcolor('red')
t.begin_fill()
t.right(90)
t.forward(80 * math.sqrt(3))
t.left(90)
t.forward(80)
t.left(120)
t.forward(160)
t.end_fill()
t.done()